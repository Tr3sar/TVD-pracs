package ejemplos;

public class Bucles_FOR {
    public static void main(String[] args) {
        int x;

        // BUCLE FOR (sin anidamiento)
        System.out.println("Empieza bucle FOR:");
        for (x=1;x<=10;x++)
        {
            System.out.print(" "+x);
        }
        System.out.println();
    }
}
